class Arithmatic01 {
    public static void main(String[] args) {
        int a = 7;
        int b = 2;

        System.out.println("Value of a: " + a);
        System.out.println("Value of b: " + b);

        // Addition
        System.out.println("a + b = " + (a + b));

        // Subtraction
        System.out.println("a - b = " + (a - b));

        // Multiplication
        System.out.println("a * b = " + (a * b));

        // Division
        System.out.println("a / b = " + (a / b));

        // Modulus
        System.out.println("a % b = " + (a % b));
    }
}