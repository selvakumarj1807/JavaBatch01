public class Ternary07 {
    public static void main(String[] args) {
        int num1 = 30;
        int num2 = 20;

        // Using ternary operator to find the maximum of two numbers
        int max = (num1 > num2) ? num1 : num2; // 10 > 20 ? 10 : 20 

        System.out.println("The maximum number is: " + max);
    }
}
